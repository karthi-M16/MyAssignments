package week1.day3;

public class LogicalOperators {
public static void main(String[] args) {
   int m=21;
    int n=20;
    int o=19;
    // and => both &&
    // or => either ||
    
    {
        System.out.println(m>n||m<n );
        System.out.println(m>n&&m<n);
        System.out.println((m*n<n*o));
    }
}
}
