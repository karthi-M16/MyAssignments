package week1.day3;

public class IfElse {
public static void main(String[] args) {
    int num1=50;
    int num2=20;
    if (num1<num2) {
        System.out.println("turn right");
    } else {
        System.out.println("turn left");
        System.out.println(num1==num2);
        System.out.println(num1!=num2);
    }
}
}
