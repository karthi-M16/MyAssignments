package week1.day7;

public class NestedForLoops {
public static void main(String[] args) {
    for(int i=1;i<=3;i++)
        // 1 to 3 --> 3 iterations
        {
            for(int p=0;p<=2;p++){
        //0 to 2 --> 3 iterations

        //total-->3*3=9 (for loop runs for 9 times)
            
            System.out.println(i+" "+p);}
}
}}
