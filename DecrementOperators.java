package week1.day3;

public class DecrementOperators {
public static void main(String[] args) {
    int a=7;{
        System.out.println(a--);
        System.out.println(a);
        System.out.println(--a);
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        
       
    }
}
}
